    {
        if (Auth::user()) {
            return view('/user/editProfile')->with('user',auth()->user());
        } else {
            return redirect()->back();
        }
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $user = auth()->user();

        $user->name = $request['name'];
        $user->email = $request['email'];
        $user->about = $request['about'];
        $user->faction = $request['faction'];
        $user->realm = $request['realm'];
        $user->save();


        $request->session()->flash('success', 'Profile updated succesfully.');
       // return view('/user/editProfile', array('user'=>Auth::user()));
        return redirect()->back();
    }

